package com.udacity.asteroidradar.maperutile

import com.udacity.asteroidradar.Asteroid
import com.udacity.asteroidradar.domainlayer.databaselayer.AsteroidDatabaseModel

fun ArrayList<AsteroidDatabaseModel>.asDomainModel(): Array<Asteroid> {
    return map {
        Asteroid(
            id = it.id,
            codename = it.codename,
            closeApproachDate = it.closeApproachDate,
            absoluteMagnitude = it.absoluteMagnitude,
            estimatedDiameter = it.estimatedDiameter,
            relativeVelocity = it.relativeVelocity,
            distanceFromEarth = it.distanceFromEarth,
            isPotentiallyHazardous = it.isPotentiallyHazardous)
    }.toTypedArray()
}

fun Asteroid.asDataBaseModel():AsteroidDatabaseModel{
    return AsteroidDatabaseModel(
        id = this.id,
        codename = this.codename,
        closeApproachDate = this.closeApproachDate,
        absoluteMagnitude = this.absoluteMagnitude,
        estimatedDiameter = this.estimatedDiameter,
        relativeVelocity = this.relativeVelocity,
        distanceFromEarth = this.distanceFromEarth,
        isPotentiallyHazardous = this.isPotentiallyHazardous)
}
